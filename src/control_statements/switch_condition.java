package control_statements;

public class switch_condition 
{
	public static void main(String[] args) 
	{
		int yr = 3;
		switch (yr) {
		case 1: System.out.println("welcome to fy-btech");
			break;
			
		case 2: System.out.println("welcome to sy-btech");
			break;
			
		case 3: System.out.println("welcome to ty-btech");
			break;
			
		case 4: System.out.println("welcome to btech");
			break;
			
		default:System.out.println("please enter values between 1-4");
			break;
		}

	}

}
