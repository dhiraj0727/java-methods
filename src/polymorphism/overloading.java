package polymorphism;

public class overloading {

					//METHOD OVERLOADING
					
	public static void main(String[] args) {

		
	}
	public void add(int a,int b) {
		int sum = a+b;
		System.out.println("sum is "+sum);
	}

	public void add(int a,int b,int c) {
		int sum = a+b+c;
		System.out.println("sum is "+sum);
		
	}
}
