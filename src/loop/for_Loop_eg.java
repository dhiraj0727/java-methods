package loop;



public class for_Loop_eg 
{
public static void main(String[] args) {
	// 1--->10
	//start point 1
	//condition <=10
	// increment ++ decrement --
	
	for (int i= 1; i<=10;i++)  // increment 1--->20
	{
		System.out.println(i);
	}
	
	// 10---->1
	//start point 10
	// condition >=20
	//
	
	for (int a =10 ; a >=1; a--)
	{
		System.out.println(a);
	}
	
	
	
	
	
	
}
}
